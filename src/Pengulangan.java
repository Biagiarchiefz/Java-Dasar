import java.util.Scanner;

public class Pengulangan {
    public static void main(String[] args) {
        int X, N;

        Scanner sc = new Scanner(System.in);
        System.out.print("Nilai N: ");N=sc.nextInt();
        X=0;
        while (X < N) {
            System.out.println("X= "+X);
            X=X=2;
        }
        System.out.print("Selesai");

    }
}
